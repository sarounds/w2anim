#
# This source file contains arrays and hashes that are needed for
# retrieving data via USGS Water Data APIs.  It was more straightforward
# to bring this information into the code rather than to read it from
# an external file.
#
# @usgs_pcodes:
#   This is the official list of USGS parameter codes that have been
#   activated for time-series datasets.
#
# %usgs_pcode_groups:
#   The pcodes in @usgs_pcodes have official USGS groups that are
#   convenient for display purposes in program menus.
#
# %usgs_pcode_names:
#   A hash that associates each parameter code with its official name,
#   where some names were shortened (by me) because they were rather long.
#
# %state_code:
#   A hash associating various U.S. States and territories with their
#   2-digit codes for data retrieval from USGS Water Data APIs.
#
# %site_type_codes:
#   A hash associating various USGS-designated site types to their
#   2-letter codes for data retrieval from USGS Water Data APIs.
#
# %huc_region:
#   A list of the 2-digit Hydrologic Unit Codes for the major regions.
#
# %huc_subregion:
#   The list of 4-digit Hydrologic Unit Codes for subregions.
#
# %huc_units:
#   A list of the 6-digit Hydrologic Unit Codes for the major basins,
#   along with the associated sub-basin names for each basin, thus
#   providing all of the 8-digit HUCs and their associated names.
#

#
